class UserModel {
  final Object object;
  final String name;
  final String email;
  final String number;
  final String? profile;
  final String role;
  final String uid;

  UserModel({
    required this.object,
    required this.name,
    required this.email,
    required this.number,
    required this.profile,
    required this.role,
    required this.uid,
  });
}
